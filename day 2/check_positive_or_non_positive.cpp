// C++ program to find if a number is positive or non-positive

#include <iostream>
using namespace std;

int main()
{
    int number = -1;

    // Using if-else to determine if the number is positive or non-positive
    if (number > 0)
    {
        cout << "The number is positive" << endl;
    }
    else
    {
        cout << "The number is non-positive" << endl;
    }

    return 0;
}
