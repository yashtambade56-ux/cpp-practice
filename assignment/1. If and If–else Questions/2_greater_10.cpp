//Write a program to check whether a number is greater than 10.

#include <iostream>
using namespace std;

int main() {
    int n = 15;

    // Check if the number is greater than 10
    if (n > 10) {
        cout << n << " is greater than 10." << endl;
    } else {
        cout << n << " is not greater than 10." << endl;
    }

    return 0;
}