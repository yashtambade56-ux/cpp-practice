// Write a program to check whether a number is zero or non-zero.

#include <iostream>
using namespace std;

int main() {
    int n = 0;

    // Check if the number is zero or non-zero
    if (n == 0) {
        cout << n << " is zero." << endl;
    } else {
        cout << n << " is non-zero." << endl;
    }

    return 0;
}